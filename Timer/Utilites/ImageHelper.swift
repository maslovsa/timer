import UIKit

extension UIImage {
    class func getPlayIcon() -> UIImage {
        return UIImage(named: "Play")!
    }
    
    class func getPauseIcon() -> UIImage {
        return UIImage(named: "Pause")!
    }
    
    class func getResetIcon() -> UIImage {
        return UIImage(named: "Reset")!
    }

    class func getMenuIcon() -> UIImage {
        return UIImage(named: "Menu")!
    }
    
}

